import { readdir, readFile, mkdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import YAML from 'yaml';

const root = process.cwd();
const schemaRoot = path.join(root, 'game/example-module/content/component-schemas');
const output = path.join(root, 'game/example-module/shared/src/generated/components.ts');

const files = [];
async function walk(directory) {
  for (const entry of await readdir(directory, { withFileTypes: true })) {
    const full = path.join(directory, entry.name);
    if (entry.isDirectory()) {
      await walk(full);
    } else if (/\.ya?ml$/i.test(entry.name)) {
      files.push(full);
    }
  }
}
await walk(schemaRoot);
files.sort();

const schemas = [];
for (const file of files) {
  const document = YAML.parse(await readFile(file, 'utf8'));
  if (document?.type !== 'component-schema') continue;
  schemas.push(document);
}

const lines = [
  '// Generated by tools/generate-component-types.mjs. Do not edit by hand.',
  '',
];
for (const schema of schemas) {
  lines.push(`export type ${identifier(schema.id)}Component = {`);
  for (const [fieldName, field] of Object.entries(schema.fields ?? {})) {
    const optional = field.required === false ? '?' : '';
    lines.push(`  readonly ${JSON.stringify(fieldName)}${optional}: ${tsType(field.type)};`);
  }
  lines.push('};', '');
}
lines.push('export const ComponentSchemas = {');
for (const schema of schemas) {
  lines.push(`  ${identifier(schema.id)}: ${JSON.stringify(schema, null, 2).replaceAll('\n', '\n  ')},`);
}
lines.push('} as const;', '');

await mkdir(path.dirname(output), { recursive: true });
await writeFile(output, lines.join('\n'));
console.log(`Generated ${schemas.length} component types.`);

function identifier(value) {
  const cleaned = String(value).replace(/[^A-Za-z0-9_$]/g, '_');
  return /^[A-Za-z_$]/.test(cleaned) ? cleaned : `_${cleaned}`;
}

function tsType(type) {
  switch (type) {
    case 'float':
    case 'int':
    case 'number':
      return 'number';
    case 'bool':
    case 'boolean':
      return 'boolean';
    case 'string':
      return 'string';
    case 'entity':
      return 'number';
    case 'json':
      return 'unknown';
    default:
      if (String(type).startsWith('array<')) {
        return `readonly ${tsType(String(type).slice(6, -1))}[]`;
      }
      return 'unknown';
  }
}
